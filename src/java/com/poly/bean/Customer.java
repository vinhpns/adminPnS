package com.poly.bean;

/**
 * @author VinhNT
 */
public class Customer {

    private String accountId;
    private String avatar;
    private String backIdCard;
    private String frontIdCard;
    private int socialIdNumber;
    private int rankId;
    private int point;
    private String bankBranch;
    private String bankName;
    private String bankNameHolder;
    private String bankNumber;

    public Customer() {
    }

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getBackIdCard() {
        return backIdCard;
    }

    public void setBackIdCard(String backIdCard) {
        this.backIdCard = backIdCard;
    }

    public String getFrontIdCard() {
        return frontIdCard;
    }

    public void setFrontIdCard(String frontIdCard) {
        this.frontIdCard = frontIdCard;
    }

    public int getSocialIdNumber() {
        return socialIdNumber;
    }

    public void setSocialIdNumber(int socialIdNumber) {
        this.socialIdNumber = socialIdNumber;
    }

    public int getRankId() {
        return rankId;
    }

    public void setRankId(int rankId) {
        this.rankId = rankId;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }

    public String getBankBranch() {
        return bankBranch;
    }

    public void setBankBranch(String bankBranch) {
        this.bankBranch = bankBranch;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public String getBankNameHolder() {
        return bankNameHolder;
    }

    public void setBankNameHolder(String bankNameHolder) {
        this.bankNameHolder = bankNameHolder;
    }

    public String getBankNumber() {
        return bankNumber;
    }

    public void setBankNumber(String bankNumber) {
        this.bankNumber = bankNumber;
    }


}
